/// Class containing padding constants used throughout the application.
///
/// Provides standardized padding values to maintain consistent spacing
/// across the app's UI components.
class AppPadding {
  // Private constructor to prevent instantiation
  AppPadding._();

  /// Smallest padding value - 2.0
  static const double xxs = 2;

  /// Extra small padding value - 4.0
  static const double xs = 4;

  /// Small padding value - 8.0
  static const double sm = 8;

  /// Medium padding value - 16.0
  static const double md = 16;

  /// Large padding value - 24.0
  static const double lg = 24;

  /// Extra large padding value - 32.0
  static const double xl = 32;

  /// Largest padding value - 64.0
  static const double xxl = 64;
}
