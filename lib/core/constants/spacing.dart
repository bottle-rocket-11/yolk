/// Class containing spacing constants used throughout the application.
///
/// Provides standardized spacing values to maintain consistent spacing
/// across the app's UI components.
class AppSpacing {
  // Private constructor to prevent instantiation
  AppSpacing._();

  /// Smallest spacing value - 2.0
  static const double xxs = 2;

  /// Extra small spacing value - 4.0
  static const double xs = 4;

  /// Small spacing value - 8.0
  static const double sm = 8;

  /// Medium spacing value - 16.0
  static const double md = 16;

  /// Large spacing value - 24.0
  static const double lg = 24;

  /// Extra large spacing value - 32.0
  static const double xl = 32;

  /// Largest spacing value - 64.0
  static const double xxl = 64;
}
